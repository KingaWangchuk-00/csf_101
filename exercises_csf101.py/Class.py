class Dog:
    def __init__(self,name, color, age):
        self.name = name
        self.color = color
        self.age = age
dawa = Dog("Dawa", "white", 55)
nima = Dog("nima", "colorless", 90)
blacky = Dog("blacky", "black", 100)

print(dawa.name)
print(dawa.age)
print(dawa.color)
print(nima.name)
print(nima.color)
print(nima.age)
print(blacky.name)
print(blacky.age)
print(blacky.color)
